
public class Weights {
	private int numDumbbells;
	private int numBenches;
	
	public Weights() {
		numDumbbells = 0;
		numBenches = 0;
	}
	
	public void setDumbbellNum(int a) {
		numDumbbells = a;
	}
	
	public int getDumbellNum() {
		return numDumbbells;
	}
	
	public void setBenchesNum(int a) {
		numBenches = a;
	}
	
	public int getBenchesNum() {
		return numBenches;
	}
	
}
